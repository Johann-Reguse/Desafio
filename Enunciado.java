/*O problema

A ProWay vai realizar um treinamento para uma grande empresa de TI de Blumenau, especializada em softwares de gestão.
O treinamento será realizado em 2 etapas e as pessoas serão divididas em salas com lotação variável.
Serão realizados também dois intervalos de café em 2 espaços distintos.
Você precisa criar o sistema que gerenciará este evento.

O sistema precisa permitir que:

- O cadastro de pessoas, com nome e sobrenome;          ---------------------

- O cadastro das salas do evento, com nome e lotação;          -------------------------

- O cadastro dos espaços de café pelo nome;                   ---------------------

A diferença de pessoas em cada sala deverá ser de no máximo 1 pessoa.         ----------cada nova pessoa vai para a slaa mais vazia disponível-----------
Para estimular a troca de conhecimentos, metade das pessoas precisam trocar de sala entre as duas etapas do treinamento.

Ao consultar uma pessoa cadastrada no treinamento, 
o sistema deverá retornar à sala em que a pessoa ficará em cada etapa e o espaço onde ela realizará cada intervalo de café.

Ao consultar uma sala cadastrada ou um espaço de café,
o sistema deverá retornar uma lista das pessoas que estarão naquela sala ou espaço em cada etapa do evento.

Requisitos obrigatórios:

Crie uma interface que permita:

- O cadastro de pessoas, com nome e sobrenome;

- O cadastro das salas do evento, com nome e lotação;

- O cadastro dos espaços de café com lotação;

- A consulta de cada pessoa;

- A consulta de cada sala e espaço;

Requisitos desejáveis:

- Persistência de dados;

- Testes unitários;

Critérios de avaliação:

Entrega dos requisitos obrigatórios: 6 pontos.

Documentação: 1 ponto.

Testes unitários: 1 ponto.

Separação de camadas: 1 ponto.

Persistência de dados: 1 ponto.*/